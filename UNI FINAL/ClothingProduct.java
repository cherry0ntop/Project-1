class ClothingProduct extends Product{
    private String size;
    private String fabric;

    public String getSize() {
        return this.size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getFabric() {
        return this.fabric;
    }

    public void setFabric(String fabric) {
        this.fabric = fabric;
    }

}
